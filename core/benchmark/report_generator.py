"""Benchmark Report Generator — produces formatted reports from benchmark results.

Outputs:
  - Markdown comparison tables
  - JSON exports for archival
  - Text summaries
"""

from __future__ import annotations

import json
import logging
from datetime import datetime
from typing import Any

from core.benchmark.models import (
    BenchmarkMode,
    BenchmarkReport,
    BenchmarkRun,
    RunStatus,
)

logger = logging.getLogger(__name__)


class BenchmarkReportGenerator:
    """Generates formatted reports from BenchmarkReport data."""

    @staticmethod
    def to_markdown(report: BenchmarkReport) -> str:
        """Full markdown report with summary table, per-model detail, and tasks."""
        lines: list[str] = [
            "# Multi-Model Benchmark Report",
            "",
            f"**Generated:** {report.generated_at.strftime('%Y-%m-%d %H:%M UTC') if report.generated_at else 'N/A'}",
            f"**Tasks:** {len(report.tasks)}  |  **Models:** {len(report.model_results)}",
            "",
            "---",
            "",
            "## Summary",
            "",
            "| Model | Raw | +Architecture | Gain | Raw Time | Arch Time |",
            "|-------|-----|---------------|------|----------|-----------|",
        ]

        for m in sorted(
            report.model_results,
            key=lambda x: x.average_gain,
            reverse=True,
        ):
            lines.append(
                f"| {m.model_config.name} "
                f"| {m.raw_success_rate:.0%} "
                f"| {m.arch_success_rate:.0%} "
                f"| {'+' if m.average_gain > 0 else ''}{m.average_gain:.0%} "
                f"| {m.raw_avg_elapsed:.1f}s "
                f"| {m.arch_avg_elapsed:.1f}s |"
            )

        lines.extend([
            "",
            f"**Overall:** "
            f"Raw: {report.overall_avg_raw:.0%} → "
            f"+Architecture: {report.overall_avg_arch:.0%} → "
            f"**Gain: +{report.overall_avg_gain:.0%}**",
            "",
            "---",
            "",
            "## Per-Model Detail",
            "",
        ])

        for m in report.model_results:
            lines.append(f"### {m.model_config.name}")
            lines.append("")
            lines.append(f"- **Provider:** {m.model_config.provider}")
            lines.append(f"- **Model ID:** {m.model_config.id}")
            lines.append(f"- **Raw success:** {m.raw_success_rate:.0%} ({len(m.raw_runs)} runs)")
            lines.append(f"- **Arch success:** {m.arch_success_rate:.0%} ({len(m.arch_runs)} runs)")
            lines.append(f"- **Gain:** {m.average_gain:+.0%}")
            lines.append("")

            # Raw runs detail
            if m.raw_runs:
                lines.append("| Task | Status | Time | Turns |")
                lines.append("|------|--------|------|-------|")
                for r in m.raw_runs:
                    lines.append(
                        f"| {r.task_id} "
                        f"| {'PASS' if r.is_success else 'FAIL'} "
                        f"| {r.elapsed_seconds:.1f}s "
                        f"| {r.metrics.get('raw_turns', '-')} |"
                    )
                lines.append("")

            # Architecture runs detail
            if m.arch_runs:
                lines.append("| Task | Status | Time | Tools | Hallucinated | Missing |")
                lines.append("|------|--------|------|-------|--------------|---------|")
                for r in m.arch_runs:
                    lines.append(
                        f"| {r.task_id} "
                        f"| {'PASS' if r.is_success else 'FAIL'} "
                        f"| {r.elapsed_seconds:.1f}s "
                        f"| {len(r.tool_names)} "
                        f"| {len(r.hallucinated_tools)} "
                        f"| {', '.join(r.missing_steps) if r.missing_steps else '-'} |"
                    )
                lines.append("")

        # Task definitions
        lines.extend([
            "---",
            "",
            "## Task Definitions",
            "",
        ])
        for t in report.tasks:
            lines.append(f"- **{t.id}:** {t.name}")
            lines.append(f"  - Required: {', '.join(t.required_tools)}")

        lines.append("")
        lines.append("---")
        lines.append("")
        lines.append("*Generated by JARVIS Multi-Model Benchmark Harness*")

        return "\n".join(lines)

    @staticmethod
    def to_text_summary(report: BenchmarkReport) -> str:
        """Concise text summary — one line per model."""
        lines: list[str] = []
        for m in sorted(
            report.model_results,
            key=lambda x: x.average_gain,
            reverse=True,
        ):
            gain_arrow = "+" if m.average_gain > 0 else "-" if m.average_gain < 0 else "="
            lines.append(
                f"  {m.model_config.name:20s}  "
                f"Raw: {m.raw_success_rate:5.0%}  "
                f"Arch: {m.arch_success_rate:5.0%}  "
                f"Gain: {gain_arrow} {m.average_gain:+6.0%}"
            )
        lines.append("")
        lines.append(f"  {'Average':20s}  "
                     f"Raw: {report.overall_avg_raw:5.0%}  "
                     f"Arch: {report.overall_avg_arch:5.0%}  "
                     f"Gain: + {report.overall_avg_gain:+6.0%}")
        return "\n".join(lines)

    @staticmethod
    def to_json(report: BenchmarkReport, indent: int = 2) -> str:
        """JSON export for archival."""
        return json.dumps(report.to_dict(), indent=indent)
